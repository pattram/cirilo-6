package voodoosoft.jroots.core;

import voodoosoft.jroots.debug.CDebugService;
import voodoosoft.jroots.message.*;


/**
 * Root ancestor of many classes.
 */
public abstract class CObject extends Object implements IObserver
{
   public CObject()
   {
      super();
   }

   public CObject(String name)
   {
      this();
      mName = name;
   }

   public CDebugService getDebugService()
   {
      return null;
   }

   public void setName(String name)
   {
      mName = name;
   }

   public String getName()
   {
      return mName;
   }

   public int notify(IMessage command)
   {
      return 0;
   }

   public int notify(IMessage command, String asType)
   {
      return 0;
   }

   public String toString()
   {
      return getName();
   }

   private String mName;
}
